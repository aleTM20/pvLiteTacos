package model;

/**
 *
 * @author pv_lite_team
 */
public class User {
    private int idUser;
    private String user;
    private String typeUser;
    private String name;

    public User(int idUser, String user, String typeUser, String name) {
        this.idUser = idUser;
        this.user = user;
        this.typeUser = typeUser;
        this.name = name;
    }

    public int getIdUser() {
        return idUser;
    }

    public void setIdUser(int idUser) {
        this.idUser = idUser;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getTypeUser() {
        return typeUser;
    }

    public void setTypeUser(String typeUser) {
        this.typeUser = typeUser;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
    
}
