public void llenarTabla(int compra){
    remove();
    try {
            String sql = "select  descripcion,gastado from compra  inner join gastos on gastos.idgasto=compra.idgasto  where idCompra="+compra+";";
//            System.out.println(sql);
  PreparedStatement pst=conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            Object datos[] = new Object[2];
            while (rs.next()) {
                    
                    datos[0]= rs.getString("descripcion");
                    datos[1]= rs.getFloat("gastado");
               modelo.addRow(datos);
            } 
            } catch (SQLException ex) {
            System.out.println("tabla descripcion compra"+ex.getMessage());
        }
}
